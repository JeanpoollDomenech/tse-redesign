// Helpers
export function buscarPorCedula(cedula) {
  const normalizada = cedula.trim().replace(/\s/g, "");
  return personasNacionales.find(
    (p) => p.cedula.replace(/-/g, "") === normalizada.replace(/-/g, "")
  ) || null;
}

export function buscarPorNombre(query) {
  const q = query.trim().toLowerCase();
  if (q.length < 2) return [];
  return personasNacionales.filter((p) => {
    const nombreCompleto = `${p.nombre} ${p.apellido1} ${p.apellido2}`.toLowerCase();
    return nombreCompleto.includes(q);
  });
}

export function buscarExtranjeroPorDimex(dimex) {
  const normalizado = dimex.trim().replace(/\s/g, "");
  return personasExtranjeras.find((p) => p.dimex === normalizado) || null;
}

export function calcularEdad(fechaNacimiento) {
  const hoy = new Date();
  const nacimiento = new Date(fechaNacimiento);
  let edad = hoy.getFullYear() - nacimiento.getFullYear();
  const mes = hoy.getMonth() - nacimiento.getMonth();
  if (mes < 0 || (mes === 0 && hoy.getDate() < nacimiento.getDate())) edad--;
  return edad;
}

export function formatearFecha(fecha) {
  const [year, month, day] = fecha.split("-");
  return `${day}/${month}/${year}`;
}
